ORG 00H
MOV P2,#00H
MOV P3,#00H ;8X8 CONFIG 

MOV P3,#7FH

RESTART:MOV DPTR,#HEX_VALUES
MOV R1,#8D
LOOP:CLR A 
MOVC A,@A+DPTR
MOV P2,A
ACALL NEXT_GND
INC DPTR
DJNZ R1,LOOP
SJMP RESTART


NEXT_GND:MOV A,P3
RL A
MOV P3,A
ACALL MICRO_DELAY
RET

MICRO_DELAY:
MOV TMOD,#02H
MSEC:MOV TL0,#00H
SETB TR0
JNB TF0,$
CLR TR0 
CLR TF0
RET

ORG 300H
HEX_VALUES: DB 00H,42H,00H,10H,81H,42H,3CH,00H
END